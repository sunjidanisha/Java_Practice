package java_basic.array;

public class basic_array {
    public static void main(String[] args) {
        int [] marks = new int [3];
        marks[0] = 20;
        marks[1] = 30;
        marks[2] = 25;
        marks[3] = 29;
        System.out.println(marks[0]);
        System.out.println(marks[1]);
        System.out.println(marks[2]);
        System.out.println(marks[3]);

    }
}
